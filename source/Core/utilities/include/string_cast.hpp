#include <stdexcept>
#include <string>
#include <sstream>

template<typename T>
void castTo(const std::string& s, T& out){
    throw std::invalid_argument("Conversion from std::string to unrecognized type.");
}


template<typename T>
void castFrom(const T& in, std::string& s){
    std::stringstream ss;
    ss << in;
    s = ss.str();
}